#include "../fractol.h"

int	*color(int i, int iter_max)
{
	static int	rgb[3];

	rgb[0] = i * 100 / iter_max + 155;
	rgb[1] = i * 220 / iter_max + 20;
	rgb[2] = i * 160 / iter_max + 10;
	return (rgb);
}
